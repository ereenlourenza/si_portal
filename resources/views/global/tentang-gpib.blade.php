        <h2 class="text-xl md:text-2xl font-semibold text-amber-700 mb-4">Sejarah Pembentukan GPIB</h2>
        <p class="mb-4">De Protestantsche Kerk in Nederlandsch-Indie, mewarisi jemaat-jemaat yang ditinggalkan oleh Portugis dengan wilayah pelayanannya meliputi sejumlah daerah seperti Maluku, Minahasa, Kepulauan Sunda Kecil (kini Nusa Tenggara Timur, dan sebagian Nusa Tenggara Barat khususnya Pulau Sumbawa dan sebagian Lombok), serta Pulau Jawa, Sumatra dan lainnya. </p>
        <p class="mb-4">Karena wilayah pelayanan semakin banyak dan meluas, maka cabang-cabang De Protestantsche Kerk in Nederlandsch-Indie mengalami berbagai persoalan. Pada tahun 1927 disepakati bahwa keesaan gereja harus tetap dipertahankan, namun wilayah yang memiliki kekhususan diberi status mandiri yang lebih luas untuk mengatur pelayanannya secara sendiri-sendiri.</p>
        <p class="mb-4">Pada tahun 1934, jemaat di Minahasa dilembagakan menjadi gereja mandiri pertama dengan nama Gereja Masehi Injili di Minahasa (GMIM). Setahun kemudian pada tahun 1935, jemaat di Maluku dilembagakan menjadi gereja mandiri kedua dengan nama Gereja Protestan Maluku (GPM). Setelah berakhirnya Perang Dunia II, pada tahun 1947, jemaat di wilayah Sunda Kecil dilembagakan menjadi gereja mandiri ketiga dengan nama Gereja Masehi Injili di Timor (GMIT). </p>
        <p class="mb-4">Setelah kemerdekaan Indonesia, Sidang Sinode De Protestantsche Kerk in Nederlandsch-Indie yang diadakan di Buitenzorg (Bogor), menyepakati bahwa gereja mandiri keempat akan dibentuk dengan wilayah pelayanan di bagian barat Indonesia. </p>
        <p class="mb-4">Pada tanggal 31 Oktober 1948 dalam Ibadah Hari Minggu Jemaat di "Willemskerk" (sekarang GPIB Immanuel Jakarta), dilembagakanlah gereja mandiri keempat yang pada waktu itu bernama De Protestantsche Kerk in Westelijk Indonesie (Gereja Protestan di Indonesia bagian Barat), berdasarkan Tata Gereja dan Peraturan Gereja yang dipersembahkan oleh proto-Sinode kepada Algemene Moderamen De Protestantsche Kerk in Nederlandsch-Indie (Badan Pekerja Am Gereja Protestan di Indonesia).</p>
    
